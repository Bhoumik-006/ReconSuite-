def parse_args():
    parser = argparse.ArgumentParser(
        description="ReconSuite v2.0 — Full-Spectrum Reconnaissance Toolkit",
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog="""Examples:
  recon_cli.py example.com
  recon_cli.py example.com --only ports,headers,ssl
  recon_cli.py example.com --skip emails --output json,md,pdf
  recon_cli.py --targets targets.txt --proxy socks5://127.0.0.1:9050
  recon_cli.py example.com --screenshots --cve""")
    parser.add_argument("target", nargs="?", help="Target domain or IP")
    parser.add_argument("--targets", help="Path to targets.txt file")
    parser.add_argument("--only", help="Comma-separated modules to run")
    parser.add_argument("--skip", help="Comma-separated modules to skip", default="screenshots")
    parser.add_argument("--output", help="Output formats: json,md,pdf", default="json,md")
    parser.add_argument("--proxy", help="Proxy URL (HTTP/HTTPS/SOCKS5)")
    parser.add_argument("--tor", action="store_true", help="Route through Tor SOCKS5")
    parser.add_argument("--screenshots", action="store_true", help="Capture screenshots")
    parser.add_argument("--cve", action="store_true", help="CVE correlation")
    parser.add_argument("--threads", type=int, default=50, help="Max threads")
    return parser.parse_args()

def resolve_modules(args) -> Dict[str, bool]:
    all_modules = ["whois", "dns", "subdomains", "subdomain_brute", "subdomain_passive",
                   "ports", "headers", "fingerprint", "ssl", "emails", "email_google",
                   "email_bing", "shodan", "crawl", "cve", "screenshots"]
    defaults = {m: True for m in all_modules}
    defaults["screenshots"] = False
    defaults["subdomain_brute"] = True
    defaults["subdomain_passive"] = True
    defaults["email_google"] = True
    defaults["email_bing"] = True
    defaults["cve"] = False
    if args.only:
        only_set = set(args.only.lower().split(","))
        for m in all_modules:
            defaults[m] = m in only_set
        return defaults
    if args.skip:
        skip_set = set(args.skip.lower().split(","))
        for m in all_modules:
            if m in skip_set:
                defaults[m] = False
    if args.screenshots:
        defaults["screenshots"] = True
    if args.cve:
        defaults["cve"] = True
    return defaults

