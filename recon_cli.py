def scan_port(host: str, port: int, timeout: float = 1.5) -> Optional[int]:
    try:
        with socket.socket(socket.AF_INET, socket.SOCK_STREAM) as s:
            s.settimeout(timeout)
            if s.connect_ex((host, port)) == 0:
                return port
    except Exception:
        pass
    return None

def port_scan(domain: str, ports: Optional[List[int]] = None, timeout: float = 1.5, progress_callback=None) -> Dict:
    target_ports = ports or TOP_PORTS
    result = {"host": domain, "open_ports": [], "total_scanned": len(target_ports)}
    def _scan_batch(port_list):
        return [p for p in port_list if scan_port(domain, p, timeout)]
    batch_size = 100
    batches = [target_ports[i:i+batch_size] for i in range(0, len(target_ports), batch_size)]
    completed = [0]
    total = len(batches)
    with concurrent.futures.ThreadPoolExecutor(max_workers=20) as ex:
        for f in concurrent.futures.as_completed([ex.submit(_scan_batch, b) for b in batches]):
            try:
                result["open_ports"].extend(f.result())
            except Exception:
                pass
            completed[0] += 1
            if progress_callback:
                progress_callback(completed[0], total)
    result["open_ports"] = sorted(set(result["open_ports"]))
    return result

