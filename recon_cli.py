    from reportlab.lib.styles import getSampleStyleSheet, ParagraphStyle
    from reportlab.lib.units import mm
    from reportlab.platypus import Paragraph, SimpleDocTemplate, Spacer
    doc = SimpleDocTemplate(filepath, pagesize=A4,
                            rightMargin=15*mm, leftMargin=15*mm,
                            topMargin=15*mm, bottomMargin=15*mm)
    styles = getSampleStyleSheet()
    story = []
    ts = ParagraphStyle("Title2", parent=styles["Title"], fontSize=22, spaceAfter=12)
    cs = ParagraphStyle("Code", parent=styles["Normal"], fontName="Courier", fontSize=8, leading=10)

    story.append(Paragraph("ReconSuite v2.0 — Security Assessment Report", ts))
    story.append(Paragraph(f"<b>Target:</b> {data.get('target', 'N/A')}", styles["Normal"]))
    story.append(Paragraph(f"<b>Scan Date:</b> {data.get('date', '')}", styles["Normal"]))
    story.append(Spacer(1, 12))

    story.append(Paragraph("1. Executive Summary", styles["Heading1"]))
    total = 0
    sev_count = {"critical": 0, "high": 0, "medium": 0, "low": 0, "info": 0}
    for s in data.get("sections", []):
        for f in s.get("findings", []):
            total += 1
            sev = f.get("severity", "info").lower()
            if sev in sev_count: sev_count[sev] += 1
    story.append(Paragraph(f"Total issues: {total}", styles["Normal"]))
    story.append(Paragraph(f"Critical: {sev_count['critical']} | High: {sev_count['high']} | "
                           f"Medium: {sev_count['medium']} | Low: {sev_count['low']} | Info: {sev_count['info']}",
                           styles["Normal"]))
    story.append(Spacer(1, 12))

    for section in data.get("sections", []):
        story.append(Paragraph(f"2. {section['title']}", styles["Heading1"]))
        for finding in section.get("findings", []):
            sev = finding.get("severity", "info").upper()
            story.append(Paragraph(f"<b>[{sev}]</b> {finding.get('title', '')}", styles["Heading2"]))
            story.append(Paragraph(finding.get("description", ""), styles["Normal"]))
            if finding.get("recommendation"):
                story.append(Paragraph(f"<b>Recommendation:</b> {finding['recommendation']}", styles["Normal"]))
            if finding.get("details"):
                story.append(Paragraph(f"<b>Details:</b> {finding['details']}", cs))
            story.append(Spacer(1, 8))

    story.append(Spacer(1, 20))
    story.append(Paragraph(DISCLAIMER.replace("---\n", ""), cs))
    doc.build(story)
    return filepath

