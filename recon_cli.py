# MAIN ENTRY POINT
# ═════════════════════════════════════════════════════════════════════
def main():
    args = parse_args()
    configure_proxy(args.proxy, args.tor)
    targets = []

    if args.targets:
        try:
            with open(args.targets, "r") as f:
                targets = [line.strip() for line in f if line.strip() and not line.startswith("#")]
        except FileNotFoundError:
            log_err(f"Targets file not found: {args.targets}")
            sys.exit(1)
    elif args.target:
        targets = [args.target]
    else:
        log_err("No target specified. Provide a domain or use --targets")
        sys.exit(1)

    output_formats = [f.strip().lower() for f in args.output.split(",")]
    modules = resolve_modules(args)

    log_info(f"Modules enabled: {', '.join(k for k, v in modules.items() if v)}")
    log_info(f"Output formats: {', '.join(output_formats)}")

    for target in targets:
        print(f"\n{'='*60}")
        log_info(f"Scanning target: {target}")
        print(f"{'='*60}")
        log_info("Running all modules in parallel...")
        results = full_scan(target, modules)
        duration = results["scan_duration"]
        log_ok(f"Scan completed in {duration}s")

        # Print summary
        for mod, data in results["modules"].items():
            if isinstance(data, dict):
                if mod == "whois":
                    if data.get("registrar"):
                        log_ok(f"[WHOIS] Registrar: {data['registrar']}")
                elif mod == "subdomains":
                    log_ok(f"[Subdomains] {len(data.get('all',[]))} found ({len(data.get('bruteforce',[]))} brute, {len(data.get('passive',[]))} passive)")
                elif mod == "ports":
                    log_ok(f"[Ports] {len(data.get('open_ports',[]))} open")
                elif mod == "headers":
                    log_ok(f"[Headers] Score: {data.get('score', 'N/A')}/100")
                elif mod == "ssl":
                    if data.get("expired"):
                        log_err("[SSL] CERTIFICATE EXPIRED!")
                elif mod == "emails":
                    log_ok(f"[Emails] {len(data.get('emails',[]))} found")
                elif mod == "cve":
                    log_ok(f"[CVE] {len(data.get('findings',[]))} CVEs matched")
                elif mod == "web_crawl":
                    log_ok(f"[Crawl] {len(data.get('findings',[]))} interesting paths")
                elif mod == "shodan" and data.get("data"):
                    log_ok(f"[Shodan] {len(data['data'].get('ports',[]))} ports, {len(data['data'].get('vulnerabilities',[]))} vulns")

        safe_name = re.sub(r"[^a-zA-Z0-9]", "_", target)
        exported = export_reports(results, safe_name, output_formats)
        for fmt, path in exported.items():
            log_ok(f"Report saved: {path}")

        log_info(f"Scan of {target} complete.")

    print(f"\n{Fore.GREEN}All scans complete. Reports saved to {OUTPUT_DIR}/{Style.RESET_ALL}")
    print(f"{Fore.MAGENTA}{DISCLAIMER}{Style.RESET_ALL}")

if __name__ == "__main__":
    main()
