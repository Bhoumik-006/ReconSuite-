def ssl_analysis(domain: str, port: int = 443) -> Dict:
    result = {"host": domain, "port": port, "error": None}
    try:
        ctx = ssl.create_default_context()
        with socket.create_connection((domain, port), timeout=10) as sock:
            with ctx.wrap_socket(sock, server_hostname=domain) as ssock:
                cert = ssock.getpeercert()
                subject, issuer = {}, {}
                try:
                    subject = dict(cert.get("subject", []))
                    issuer = dict(cert.get("issuer", []))
                except (TypeError, ValueError):
                    pass # Handle malformed cert fields
                result.update({
                    "subject": subject,
                    "issuer": issuer,
                    "version": cert.get("version"),
                    "serial": cert.get("serialNumber"),
                    "not_before": cert.get("notBefore"),
                    "not_after": cert.get("notAfter"),
                    "san": cert.get("subjectAltName", []),
                    "cipher": ssock.cipher(),
                    "protocol": ssock.version(),
                })
                not_after = cert.get("notAfter", "")
                if not_after:
                    try:
                        expiry = datetime.strptime(not_after, "%b %d %H:%M:%S %Y %Z").replace(tzinfo=timezone.utc)
                        now = datetime.now(timezone.utc)
                        days_left = (expiry - now).days
                        result["days_remaining"] = days_left
                        result["expired"] = days_left < 0
                        result["expiring_soon"] = 0 <= days_left < 30
                    except ValueError:
                        pass
    except Exception as e:
        result["error"] = str(e)
    return result

