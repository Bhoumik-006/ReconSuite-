# SCAN ORCHESTRATOR
# ═════════════════════════════════════════════════════════════════════
def full_scan(domain: str, modules: Dict[str, bool], progress_callback=None) -> Dict:
    import ipaddress
    start = time.time()
    results = {"target": domain, "date": datetime.now().strftime("%Y-%m-%d %H:%M:%S"),
               "scan_duration": 0, "modules": {}}
    threads = {}

    is_ip = False
    try:
        ipaddress.ip_address(domain)
        is_ip = True
    except ValueError:
        pass

    # Disable modules that don't work on IPs
    if is_ip:
        modules["subdomains"] = modules["subdomain_brute"] = modules["subdomain_passive"] = False

    # --- Define module-specific progress handlers ---
    def create_sub_progress_handler(module_name, weight):
        def handler(completed, total):
            if progress_callback:
                # This is a simplified weighted progress. A more complex system
                # could be built, but this gives a good sense of movement.
                # We report the main module name, but the progress is for the sub-task.
                progress_callback(module_name, completed, total)
        return handler

    if modules.get("whois", True):
        threads["whois"] = threading.Thread(target=lambda: results["modules"].update(
            {"whois": whois_lookup(domain)}))
    if modules.get("dns", True):
        threads["dns"] = threading.Thread(target=lambda: results["modules"].update(
            {"dns": dns_enum(domain)}))
    if modules.get("subdomains", True):
        subdomain_progress = create_sub_progress_handler("subdomains", 4)
        threads["subdomains"] = threading.Thread(target=lambda: results["modules"].update(
            {"subdomains": subdomain_enum(domain, modules.get("subdomain_brute", True),
                                          modules.get("subdomain_passive", True),
                                          progress_callback=subdomain_progress)}))
    if modules.get("ports", True):
        port_progress = create_sub_progress_handler("ports", 5)
        threads["ports"] = threading.Thread(target=lambda: results["modules"].update(
            {"ports": port_scan(domain, progress_callback=port_progress)}))
    if modules.get("headers", True):
        threads["headers"] = threading.Thread(target=lambda: results["modules"].update(
            {"headers": check_http_headers(f"https://{domain}")}))
    if modules.get("fingerprint", True):
        threads["fingerprint"] = threading.Thread(target=lambda: results["modules"].update(
            {"tech_fingerprint": fingerprint_tech(f"https://{domain}")}))
    if modules.get("ssl", True):
        threads["ssl"] = threading.Thread(target=lambda: results["modules"].update(
            {"ssl": ssl_analysis(domain)}))
    if modules.get("emails", True):
        threads["emails"] = threading.Thread(target=lambda: results["modules"].update(
            {"emails": email_harvest(domain, modules.get("email_google", True),
                                     modules.get("email_bing", True))}))
    if modules.get("shodan", True):
        threads["shodan"] = threading.Thread(target=lambda: results["modules"].update(
            {"shodan": shodan_query(domain)}))
    if modules.get("crawl", True):
        threads["crawl"] = threading.Thread(target=lambda: results["modules"].update(
            {"web_crawl": web_crawl(domain)}))

    total_modules = len(threads)
    completed_modules = [0]
    lock = threading.Lock()

    def _run(name, t):
        t.start(); t.join()
        with lock:
            completed_modules[0] += 1
            if progress_callback:
                # When a main module thread finishes, report its completion
                # This is different from the sub-progress reported within the thread
                # For simplicity, we'll just use the module name and the overall count.
                # The UI will see progress updates for "ports" (sub-progress) and then
                # a final update for "ports" when the whole module is done.
                progress_callback(name, completed_modules[0], total_modules)

    runners = []
    for name, t in threads.items():
        rt = threading.Thread(target=_run, args=(name, t))
        rt.start(); runners.append(rt)
    for r in runners:
        r.join()
    results["scan_duration"] = round(time.time() - start, 2)

    if modules.get("cve", True):
        tech_list = results["modules"].get("tech_fingerprint", {}).get("technologies", [])
        results["modules"]["cve"] = cve_correlation(tech_list)
    if modules.get("screenshots", False):
        subdomains = results["modules"].get("subdomains", {}).get("all", [])
        ss = []
        for sd in subdomains[:10]:
            for proto in ["https", "http"]:
                p = screenshot_subdomain(f"{proto}://{sd}")
                if p:
                    ss.append({"subdomain": sd, "screenshot": p})
                    break
        results["modules"]["screenshots"] = ss
    return results

