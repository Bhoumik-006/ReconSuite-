CVE_CACHE: Dict[str, List[Dict]] = {}

def cve_search(cpe: str) -> List[Dict]:
    if cpe in CVE_CACHE:
        return CVE_CACHE[cpe]
    results = []
    try:
        resp = session.get("https://services.nvd.nist.gov/rest/json/cves/2.0",
                           params={"cpeName": cpe, "resultsPerPage": 20},
                           timeout=20)
        if resp.status_code == 200:
            for vuln in resp.json().get("vulnerabilities", []):
                cve = vuln.get("cve", {})
                metrics = cve.get("metrics", {})
                base_score, severity = None, None
                for cvss_key in ["cvssMetricV31", "cvssMetricV30", "cvssMetricV2"]:
                    if cvss_key in metrics:
                        base_score = metrics[cvss_key][0]["cvssData"]["baseScore"]
                        severity = metrics[cvss_key][0]["cvssData"]["baseSeverity"]
                        break
                results.append({
                    "id": cve.get("id"),
                    "description": cve.get("descriptions", [{}])[0].get("value", ""),
                    "score": base_score,
                    "severity": severity,
                    "published": cve.get("published"),
                    "matched_cpe": cpe,
                })
        elif resp.status_code == 403:
            log_warn("NVD API rate limited")
    except Exception as e:
        log_warn(f"CVE search error: {e}")
    CVE_CACHE[cpe] = results
    return results

def _extract_version(text: str) -> Optional[str]:
    match = re.search(r"(?<![a-zA-Z])v?(\d+(?:\.\d+){0,3}(?:[-+][0-9A-Za-z_.-]+)?)", text)
    return match.group(1) if match else None

def _versioned_cpe_for_technology(technology: str) -> Optional[Dict[str, str]]:
    tech_to_cpe = {
        "nginx": ("nginx", "nginx"),
        "apache": ("apache", "http_server"),
        "wordpress": ("wordpress", "wordpress"),
        "drupal": ("drupal", "drupal"),
        "joomla": ("joomla", "joomla"),
        "laravel": ("laravel", "laravel"),
        "django": ("djangoproject", "django"),
        "flask": ("palletsprojects", "flask"),
        "jquery": ("jquery", "jquery"),
        "react": ("facebook", "react"),
        "vue.js": ("vuejs", "vue"),
        "vue": ("vuejs", "vue"),
        "angular": ("angular", "angular"),
        "bootstrap": ("getbootstrap", "bootstrap"),
        "express": ("expressjs", "express"),
        "asp.net": ("microsoft", "asp.net"),
        "ruby on rails": ("rubyonrails", "rails"),
        "rails": ("rubyonrails", "rails"),
    }
    raw = technology.strip()
    base = raw.split(":", 1)[1].strip() if ":" in raw else raw
    lowered = base.lower()
    version = _extract_version(base)
    if not version:
        return None
    key = None
    for candidate in sorted(tech_to_cpe, key=len, reverse=True):
        if candidate in lowered:
            key = candidate
            break
    if not key:
        return None
    vendor, product = tech_to_cpe[key]
    return {
        "technology": base,
        "version": version,
        "cpe": f"cpe:2.3:a:{vendor}:{product}:{version}:*:*:*:*:*:*:*"
    }

def cve_correlation(tech_list: List[str]) -> Dict:
    result = {"technologies_checked": [], "skipped": [], "findings": []}
    seen_cpes = set()
    for tech in tech_list:
        cpe_info = _versioned_cpe_for_technology(tech)
        if not cpe_info:
            result["skipped"].append({"technology": tech, "reason": "no supported version-specific CPE"})
            continue
        cpe = cpe_info["cpe"]
        if cpe in seen_cpes:
            continue
        seen_cpes.add(cpe)
        result["technologies_checked"].append(cpe_info)
        cves = cve_search(cpe)
        for cve in cves:
            cve["technology"] = cpe_info["technology"]
            cve["version"] = cpe_info["version"]
        result["findings"].extend(cves)
    return result

