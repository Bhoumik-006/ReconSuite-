def web_crawl(domain: str, paths: Optional[List[str]] = None, follow_redirects: bool = True) -> Dict:
    base_paths = paths or COMMON_PATHS
    found = []
    def _check(path):
        for proto in ["https", "http"]:
            url = f"{proto}://{domain}{path}"
            try:
                resp = session.get(url, timeout=10, allow_redirects=follow_redirects)
                if resp.status_code in [200, 301, 302, 401, 403, 500]:
                    entry = {"url": url, "status": resp.status_code, "size": len(resp.content), "title": ""}
                    try:
                        soup = BeautifulSoup(resp.text, "html.parser")
                        t = soup.find("title")
                        if t: entry["title"] = t.get_text(strip=True)
                    except Exception:
                        pass
                    return entry
                return None
            except Exception:
                continue
        return None
    with concurrent.futures.ThreadPoolExecutor(max_workers=30) as ex:
        for f in concurrent.futures.as_completed({ex.submit(_check, p): p for p in base_paths}):
            try:
                r = f.result()
                if r: found.append(r)
            except Exception:
                pass
    found.sort(key=lambda x: x["url"])
    return {"domain": domain, "paths_checked": len(base_paths), "findings": found}

