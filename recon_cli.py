def dns_enum(domain: str) -> Dict:
    records = {}
    for rtype in ["A", "AAAA", "MX", "NS", "TXT", "SOA", "CNAME"]:
        try:
            answers = dns.resolver.resolve(domain, rtype, lifetime=10)
            records[rtype] = [str(r) for r in answers]
        except Exception:
            records[rtype] = []
    return records

